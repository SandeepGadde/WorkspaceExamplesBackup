package pattern.behavioral.chainofresponsibility;

public class Matter {
	// size of matter
	private int size;
	// quantity
	private int quantity;

	/**
	 * returns the size
	 */
	public int getSize() {
		return size;
	}

	/**
	 * sets the size
	 */
	public void setSize(int size) {
		this.size = size;
	}

	/**
	 * returns the quantity
	 */
	public int getQuantity() {
		return quantity;
	}

	/**
	 * sets the quantity
	 */
	public void setQuantity(int quantity) {
		this.quantity = quantity;
	}

}// End of class
